package Programming;

public class Substring
{
    public static void main(String[] args) {
        //This is substring which returns one part of the function we have written. Strings in java are inmutable.New strings are made always in the output
        String name="Tarun and Vani";
        System.out.println(name.substring(1,12));
        System.out.println(name.substring(0,14));
    }
}

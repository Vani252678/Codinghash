package Arrays;

public class Arrays2_2DArrays {
    public static void main(String[] args) {
        int[] marks={98,97,96};// it can directly be written also
        int[][] final_marks= {{98,97,96}, {97,96,95}};//marks stored of 2 different students
        System.out.println(final_marks[0][0]);
        System.out.println(final_marks[1][0]);
        System.out.println(final_marks[0][1]);
        System.out.println(final_marks[1][1]);




    }

}
